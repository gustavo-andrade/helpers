     */
    public static function getMaxSizeUploadFile()
    {

        $post_max = ini_get('post_max_size');
        $upload_max = ini_get('upload_max_filesize');

        if($upload_max > $post_max)
        {
            return $upload_max;
        }

        return $post_max;

