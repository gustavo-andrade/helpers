    /**
     * Tratamento rápido de Data
     */

    public static function dateTimeUSAToBR($data)
    {
        $data = new \DateTime($data);
        return $data->format("d/m/Y");
    }

    public static function dateUSAToBR($data)
    {
        return self::dateTimeUSAToBR($data);
    }

    public static function dateBRToUSA($data)
    {
        $data = \DateTime::createFromFormat('d/m/Y', $data);
        return $data->format("Y-m-d");
    }




    /**
     * TRATAMENTO DE VALOR MONETÁRIO
     */
    public static function moneyBRToUSA($valor)
    {   $valor = str_replace(".", "", $valor);
        $valor = str_replace(",", ".", $valor);
        $valor = str_replace("R$", "", $valor);

        return $valor;
    }

    public static function moneyUSAToBR($valor)
